    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        Network cn=null;
       for(Network n:system.getNetworkList())
       {
           for(Enterprise e:n.getEnterpriseDirectory().getEnterpriseList())
           {
               if(e instanceof PublicSectorEnterprise)
               {
                   if(((PublicSectorEnterprise)e).getCitizenDirectory().getCitizenList().contains(citizen))
                   {
                       cn=n;
                   }
               }
           }
       }
       for(Enterprise e:cn.getEnterpriseDirectory().getEnterpriseList())
           {
               if(e instanceof RemoteClinicEnterprise)
               {
                   remote=((RemoteClinicEnterprise)e);
               }
           }
        TravelScheduleView travelScheduleArea = new TravelScheduleView(container, remote);
        container.add("TravelScheduleArea", travelScheduleArea);
        utility.Validator.FormatPanel(travelScheduleArea,0);
        CardLayout layout = (CardLayout) container.getLayout();
        layout.next(container);
    }//GEN-LAST:event_jButton3ActionPerformed

