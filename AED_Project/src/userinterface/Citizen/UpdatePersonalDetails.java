    public void fillCombo(){
        int i =0;
        Network[] arr=new Network[system.getNetworkList().size()];
        for (Network n : system.getNetworkList()) {
            arr[i]=n;
            i++;
        }
        jComboBox1.setModel(new javax.swing.DefaultComboBoxModel(arr));
        jComboBox1.setSelectedIndex(0);
        
    }
