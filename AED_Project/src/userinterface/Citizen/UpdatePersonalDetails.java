    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            String check = "";
        
        check = utility.Validator.ValidateTextFields(ssnTxtfield,true,3,9,9);
        if(!check.equals("")){JOptionPane.showMessageDialog(null,check,"Alert",JOptionPane.WARNING_MESSAGE);return;}
        check = utility.Validator.ValidateTextFields(nameTxTField,true,0,3,25);
        if(!check.equals("")){JOptionPane.showMessageDialog(null,check,"Alert",JOptionPane.WARNING_MESSAGE);return;}
        check = utility.Validator.ValidateTextFields(lastNameTxtField,true,0,2,25);
        if(!check.equals("")){JOptionPane.showMessageDialog(null,check,"Alert",JOptionPane.WARNING_MESSAGE);return;}
        check = utility.Validator.ValidateTextFields(phoneNumberTexTField,true,3,10,10);
        if(!check.equals("")){JOptionPane.showMessageDialog(null,check,"Alert",JOptionPane.WARNING_MESSAGE);return;}
        check = utility.Validator.ValidateTextFields(dateTxtField,true,2,5,25);
        if(!check.equals("")){JOptionPane.showMessageDialog(null,check,"Alert",JOptionPane.WARNING_MESSAGE);return;}
        if(buttonGroup1.getSelection()==null){
        JOptionPane.showMessageDialog(null,"Select a Gender","Alert",JOptionPane.WARNING_MESSAGE);
        }
        check = utility.Validator.ValidateTextFields(userNameTxTFiedl,true,0,4,25);
        if(!check.equals("")){JOptionPane.showMessageDialog(null,check,"Alert",JOptionPane.WARNING_MESSAGE);return;}
        if(passwordTxtField.getPassword().length<4)
        {check="Password should be atleast 4 characters long!";}
        if(!check.equals("")){JOptionPane.showMessageDialog(null,check,"Alert",JOptionPane.WARNING_MESSAGE);return;}
        
            SimpleDateFormat dtFormat =  new SimpleDateFormat ("MM/dd/yyyy");

            c.setUserId(userNameTxTFiedl.getText());
            c.setPassword(String.valueOf(passwordTxtField.getPassword()));
            c.setContactNumber(Long.parseLong(phoneNumberTexTField.getText()));
            c.setEmailAddress(emailIdTextField.getText());
            c.setFirstName(nameTxTField.getText());
            c.setDateofBirth(dtFormat.parse(dateTxtField.getText()));
            c.setSsn(Long.parseLong(ssnTxtfield.getText()));
            c.setLastName(lastNameTxtField.getText());

            if(MaleBtn.isSelected())
            {
                c.setGender("Male");
            }
            if(Female.isSelected())
            {
                c.setGender("Female");
            }
        } catch (ParseException ex) {
            Logger.getLogger(CitizenRegisterJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }

        JOptionPane.showMessageDialog(null,"Citizen has been Updated Succesfully");

    }//GEN-LAST:event_jButton1ActionPerformed

