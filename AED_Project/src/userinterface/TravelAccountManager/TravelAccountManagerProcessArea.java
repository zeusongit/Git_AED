    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        
        
        if(resultJTextField.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter a result");
            
        }
        else if(request.getTotal()<=((RemoteClinicEnterprise)enterprise).getTotalFunds()){
        request.setResult(resultJTextField.getText());
        request.setStatus(Constants.Status.Resolved);
        JOptionPane.showMessageDialog(null,"Request Resolved/Saved");
        }
        else{
            //request.setStatus("11");
            JOptionPane.showMessageDialog(null,"Insufficient funds, requesting government for funds");
        }
    }//GEN-LAST:event_submitJButtonActionPerformed

    private void backJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backJButtonActionPerformed

        userProcessContainer.remove(this);
        Component[] componentArray = userProcessContainer.getComponents();
        Component component = componentArray[componentArray.length - 1];
        TravelAccountManagerWorkArea ltjp = (TravelAccountManagerWorkArea) component;
        ltjp.populateTable();
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_backJButtonActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(resultJTextField.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Enter Result!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
          if(request.getStatus().equals(Constants.Status.Processing) ||request.getStatus().equals(Constants.Status.Resolved)  )
        {


            ((RemoteClinicEnterprise)enterprise).getFundReqQueue().getWorkRequestList().add(request);
            request.getTrail().addTrail(Constants.Status.Forwarded, userAccount,Constants.TrailLevel.Level2);
            request.setStatus(Constants.Status.Forwarded);

            
            JOptionPane.showMessageDialog(null, "Fund Request Forwarded to Accounts!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Fund Request not pending", "Warning", JOptionPane.WARNING_MESSAGE);   
        }
    }//GEN-LAST:event_jButton1ActionPerformed


